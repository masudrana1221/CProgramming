#include <stdio.h>
int main()
{
   int a = 5;
   int b = 6;
   int result = a == b; // it is called a relational operator.
   printf("It is=> %d", result);
}

/* Relational Operator:
   (==) => Equal equal
   (<)  => Less than
   (>)  => Greater than
   (<=) => Less or equal
   (>=) => Greater or equal
   (!=) => Not equal
*/